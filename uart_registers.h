#ifndef __uart_registers_h
#define __uart_registers_h

#define UART_BASE 0x80000100
#define UART_TOP 0x80000200
#define UART_IRQ 2

#define UART_DATA_OFFSET 0x00
#define UART_STATUS_OFFSET 0x04
#define UART_CONTROL_OFFSET 0x08
#define UART_SCALER_OFFSET 0x0c

#define UART_DATA (UART_BASE + UART_DATA_OFFSET)
#define UART_STATUS (UART_BASE + UART_STATUS_OFFSET)
#define UART_CONTROL (UART_BASE + UART_CONTROL_OFFSET)
#define UART_SCALER (UART_BASE + UART_SCALER_OFFSET)

#define UART_CONTROL_ENABLE_RX 0x00000001
#define UART_CONTROL_ENABLE_TX 0x00000002

#define UART_STATUS_DR 0x00000001
#define UART_STATUS_TS 0x00000002
#define UART_STATUS_TE 0x00000004
#define UART_STATUS_BR 0x00000008
#define UART_STATUS_OV 0x00000010
#define UART_STATUS_PE 0x00000020
#define UART_STATUS_FE 0x00000040
#define UART_STATUS_TH 0x00000080
#define UART_STATUS_RH 0x00000100
#define UART_STATUS_TF 0x00000200
#define UART_STATUS_RF 0x00000400

#endif
